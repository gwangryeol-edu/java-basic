package Java_basic_practice;

public class P1_EvenOdd {
    public static void main(String[] args) {
        int num = 17;

        if (num % 2 == 0) {
            System.out.println(num + "은 짝수 입니다.");
        } else{
            System.out.println(num + "은 홀수 입니다.");
        }
    }
}


// 17은 홀수 입니다.